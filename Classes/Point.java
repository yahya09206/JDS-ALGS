public class Point {
	private int x;
	private int y;

	public Point(){

	}

	public Point(int x, int y){
		this.x = x;
		this.y = y;
	}

	public int getX(){
		return this.x;
	}

	public int getY(){
		return this.y;
	}

	public void setX(int x){
		return this.x;
	}

	public void sexY(int y){
		return this.y;
	}
}